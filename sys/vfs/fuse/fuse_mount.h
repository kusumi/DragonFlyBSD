#ifndef FUSE_MOUNT_H
#define FUSE_MOUNT_H

#include <sys/mount.h>

#define FUSE_MOUNT_DEFAULT_PERMISSIONS	0x01
#define FUSE_MOUNT_ALLOW_OTHER		0x02
#define FUSE_MOUNT_MAX_READ		0x04
#define FUSE_MOUNT_SUBTYPE		0x08

struct fuse_mount_info {
	int flags;
	int fd;
	int max_read;
	const char *subtype;
	const char *from;
};

#endif /* !FUSE_MOUNT_H */
